#Leia um número e, a seguir, 
#leia uma lista de números até achar um igual ao primeiro número lido.

def main():
    numero = int(input('Digite um numero: '))
    numero_lista = 0

    while numero != numero_lista:

        numero_lista = int(input('Digite um numero para a lista: '))

    
    print(f'Numero:{numero} ... Lista:{numero_lista}')

main()
